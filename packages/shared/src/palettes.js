const PALETTE_IDS = ['oak', 'sage', 'plum', 'cloud', 'ember', 'matrix', 'ink', 'sakura', 'ocean', 'honey'];
const DEFAULT_PALETTE = 'oak';

/**
 * Convert camelCase color keys to CSS variable format (--kebab-case)
 * @param {object} colors - Palette colors object
 * @returns {object} - Object with CSS variable keys
 */
function toCssVariables(colors) {
  const result = {};
  for (const [key, value] of Object.entries(colors)) {
    // Convert camelCase to kebab-case: bgGradient -> bg-gradient
    const cssKey = key.replace(/([A-Z])/g, '-$1').toLowerCase();
    result[`--${cssKey}`] = value;
  }
  return result;
}

/**
 * Get palette with CSS variable formatted keys
 * @param {string} paletteId - Palette identifier
 * @param {'light' | 'dark'} mode - Theme mode
 * @returns {object} - Colors with CSS variable keys
 */
function getPaletteCssVars(paletteId, mode) {
  const palette = PALETTES[paletteId] || PALETTES[DEFAULT_PALETTE];
  return toCssVariables(palette[mode] || palette.light);
}

